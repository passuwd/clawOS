.code64
.section .text

.global gdt_flush
gdt_flush:
    lgdt (%rdi)
    mov $0x10, %ax
    mov %ax, %ds
    mov %ax, %es
    mov %ax, %fs
    mov %ax, %gs
    mov %ax, %ss

    /* Far jump to load CS */
    pushq $0x08
    lea .reload_cs(%rip), %rax
    pushq %rax
    lretq

.reload_cs:
    ret

.global tss_flush
tss_flush:
    mov $0x28, %ax
    ltr %ax
    ret
